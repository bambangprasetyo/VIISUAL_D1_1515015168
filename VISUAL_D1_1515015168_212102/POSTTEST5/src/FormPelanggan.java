    private void pasangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pasangActionPerformed
        // TODO add your handling code here:
        float a;
        double O=Double.parseDouble(jTberat.getText());
        a = Float.valueOf(jSdiskon.getValue());
        double R;
        double B=11500;
        double A=1.5;
        
         if (jRreguler.isSelected()){
             R = (O*B)*a/100;
            jTtotal.setText(String.valueOf(R));
    }
        else if(jRexpres.isSelected()){
            R = ((O*B)*A)*a/100;
            jTtotal.setText(String.valueOf(R));
    }
        
    }//GEN-LAST:event_pasangActionPerformed

