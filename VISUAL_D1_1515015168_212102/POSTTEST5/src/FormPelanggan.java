    private void jTteleponKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTteleponKeyPressed
        // TODO add your handling code here:
          jTtelepon.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e){
            char karakter1 = e.getKeyChar();
            if(!((karakter1 >= '0') && (karakter1 <= '9')) || (karakter1 == '\b')){
                e.consume();
            }
            }
            
}); 
    }//GEN-LAST:event_jTteleponKeyPressed

    private void jTberatKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTberatKeyPressed
        // TODO add your handling code here:
               jTberat.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e){
            char karakter = e.getKeyChar();
            if(((karakter < '0') || (karakter > '9')) && (karakter != '.')){
                e.consume();
            }
            }
            
});
    }//GEN-LAST:event_jTberatKeyPressed

