    private void jTnamaCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jTnamaCaretUpdate
        // TODO add your handling code here:
        if(jTnama.getText().length()== 0 || jTberat.getText().equals("0")){
         pasang.setEnabled(false);
        }
        else if
            (jTnama.getText().length() !=0 && jTtelepon.getText().length() !=0 && jTberat.getText().length() !=0){
            pasang.setEnabled(true);
        }
    }//GEN-LAST:event_jTnamaCaretUpdate

